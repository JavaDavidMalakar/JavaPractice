package practice3;

public class ForLoopPractice {

    public static void main(String[] args) {
        for (int i = 0; i <= 7; i++) {
            System.out.println("Good night"+ i);
        }
        for (int i = 0; i >-5 ; i--) {

            System.out.println("Hello Guys"+ i);

        }
        for (int i = 0; i >-7 ; i--) {

            System.out.println("Java Lover"+ i);

        }

    }


}


